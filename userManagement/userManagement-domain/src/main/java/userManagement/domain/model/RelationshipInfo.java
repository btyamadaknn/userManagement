package userManagement.domain.model;

/**
 * @author terafw0684
 *
 */
public class RelationshipInfo {
    
    private int relationshipNumber;
    private String relationshipCode;
    private String relationshipName;
    private int relationshipAge;
    private String relationshipGender;
    /**
     * @return the relationshipNumber
     */
    public int getRelationshipNumber() {
        return relationshipNumber;
    }
    /**
     * @param relationshipNumber the relationshipNumber to set
     */
    public void setRelationshipNumber(int relationshipNumber) {
        this.relationshipNumber = relationshipNumber;
    }
    /**
     * @return the relationshipCode
     */
    public String getRelationshipCode() {
        return relationshipCode;
    }
    /**
     * @param relationshipCode the relationshipCode to set
     */
    public void setRelationshipCode(String relationshipCode) {
        this.relationshipCode = relationshipCode;
    }
    /**
     * @return the relationshipName
     */
    public String getRelationshipName() {
        return relationshipName;
    }
    /**
     * @param relationshipName the relationshipName to set
     */
    public void setRelationshipName(String relationshipName) {
        this.relationshipName = relationshipName;
    }
    /**
     * @return the relationshipAge
     */
    public int getRelationshipAge() {
        return relationshipAge;
    }
    /**
     * @param relationshipAge the relationshipAge to set
     */
    public void setRelationshipAge(int relationshipAge) {
        this.relationshipAge = relationshipAge;
    }
    /**
     * @return the relationshipGender
     */
    public String getRelationshipGender() {
        return relationshipGender;
    }
    /**
     * @param relationshipGender the relationshipGender to set
     */
    public void setRelationshipGender(String relationshipGender) {
        this.relationshipGender = relationshipGender;
    }
     
}
